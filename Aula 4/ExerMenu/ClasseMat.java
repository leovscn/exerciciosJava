package ExerMenu;

public class ClasseMat {

  public static int dobro(int n) {
    return (n * 2);
  }

  public static int triplo(int n) {
    return (n * 3);
  }

  public static int mult(int n, int n2) {
    return (n * n2);
  }

  public static int pot(int n, int n2) {
    return ((int) Math.pow(n, n2));
  }
}
